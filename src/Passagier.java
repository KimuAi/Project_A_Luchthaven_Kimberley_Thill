public class Passagier {
    private String naam;
    private int leeftijd;


    public Passagier(String naam, int leeftijd) {
        this.naam = naam;
        this.leeftijd = leeftijd;

    }

    public String getNaam() {
        return naam;
    }

    public int getLeeftijd() {
        return leeftijd;
    }
}